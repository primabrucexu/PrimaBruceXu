    public static int shortestBridge(int[][] A) {
        Queue<int[]> queue = new LinkedList<>();
        Queue<int[]> island = new LinkedList<>();
        int[][] direction = new int[][]{{-1, 0}, {1, 0}, {0, -1}, {0, 1}};
        int row = A.length;
        int column = A[0].length;
        // bfs找到第一个岛
        boolean sign = false;
        for (int i = 0; i < row; i++) {
            if (sign) {
                break;
            }
            for (int j = 0; j < column; j++) {
                if (A[i][j] == 1) {
                    queue.offer(new int[]{i, j});
//                     bfs(queue, island, A, direction);
                    dfs(island, A, i, j);
                    sign = true;
                    break;
                }
            }
        }

        // bfs查找另外一个岛
        int len = -1;
        while (!island.isEmpty()) {
            int size = island.size();
            len++;
            while (size-- > 0) {
                int[] p = island.poll();
                for (int[] i : direction) {
                    int newR = p[0] + i[0];
                    int newC = p[1] + i[1];
                    if (newR >= 0 && newR < row && newC >= 0 && newC < column) {
                        if (A[newR][newC] == 2) {
                            continue;
                        }
                        if (A[newR][newC] == 1) {
                            return len;
                        }
                        A[newR][newC] = 2;
                        island.offer(new int[]{newR, newC});
                    }
                }
            }
        }
        return len;
    }

    private static void dfs(Queue<int[]> island, int[][] map, int row, int column) {
        if (row < 0 || column < 0 || row == map.length || column == map[0].length || map[row][column] == 2 || map[row][column] == 0) {
            return;
        }
        if (map[row][column] == 1) {
            map[row][column] = 2;
            island.offer(new int[]{row, column});
        }
        dfs(island, map, row - 1, column);
        dfs(island, map, row + 1, column);
        dfs(island, map, row, column - 1);
        dfs(island, map, row, column + 1);
    }

    private static void bfs(Queue<int[]> queue, Queue<int[]> island, int[][] map, int[][] direction) {
        while (!queue.isEmpty()) {
            int[] p = queue.poll();
            island.offer(p);
            int r = p[0];
            int c = p[1];
            map[r][c] = 2;
            for (int[] i : direction) {
                int newR = r + i[0];
                int newC = c + i[1];
                if (newR >= 0 && newR < map.length && newC >= 0 && newC < map[0].length && map[newR][newC] == 1) {
                    queue.offer(new int[]{newR, newC});
                }
            }
        }
    }
